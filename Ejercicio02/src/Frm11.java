    private void BtnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnMostrarActionPerformed
        // TODO add your handling code here:
       
     try {
          int Numero= Integer.parseInt(TxtNumeroo.getText());
      
       if(Numero<=0){
            JOptionPane.showMessageDialog(this, "No se aceptan numeros negativos",
                "No v치lido", JOptionPane.ERROR_MESSAGE);
        }
       if(Numero>=100 && Numero<999){
        int digito1= Numero % 10;
        int digito2 = (Numero/10)%10;
        int digito3 = Numero/100;
        TxtResultadoo.setText(digito1+","+digito2+","+digito3);
    }
         if(Numero>=10 && Numero<=99){
        int digito1= Numero % 10;
        int digito2 = (Numero/10)%10;
        int digito3 = Numero/100;
        TxtResultadoo.setText(digito1+","+digito2);
    }
         String num= TxtNumeroo.getText();
           if (num.length()<2){
            
        JOptionPane.showMessageDialog(this, "No se aceptan menores de 2 digitos",
                "No v치lido", JOptionPane.ERROR_MESSAGE);
    }
             
        if(num.length()>3){
            JOptionPane.showMessageDialog(this, "No se aceptan mayores de 3 digitos",
                "No v치lido", JOptionPane.ERROR_MESSAGE);
        }
     }catch (NumberFormatException e ){
          JOptionPane.showMessageDialog(this, "NO LETRAS",
                "No v치lido", JOptionPane.ERROR_MESSAGE);
     }
    }//GEN-LAST:event_BtnMostrarActionPerformed

